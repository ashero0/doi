<h1>Sexual Assault</h1>
<p>Sexual assault is a major problem on college campuses around the world. Twenty percent of college women, as well as some
	men, are estimated to have been raped. In one study, over 90 percent of college men and women indicated that they have experience
	unwanted sexual activity. In most cases of sexual assault, the assailant knows the victim.</p>
<p>
	<b>According to the Women's Center at Virginia Tech, sexual assault is:</b>
</p>
<ul>
	<li>a broad term that encompasses any sexual activity that occurs without the victim's
		<a href="http://stopabuse.vt.edu/Get_Educated.html"
		 target="_blank">consent</a>.</li>
	<li>a range of behaviors that include, but are not limited to, unwanted kissing and fondling, forcible vaginal, oral, or anal
		intercourse, forcible vaginal, oral or anal penetration with an object or a finger.</li>
	<li>about exerting power and control over someone else, not about out of control sexual desire. The perpetrator makes a choice
		to meet his or her needs at the expense of the other person's needs.</li>
	<li>not gender specific. Sexual assaults can occur between anyone with any gender identity. At Virginia Tech, most (but not
		all) perpetrators of sexual assault are men and most (but not all) victims are women.</li>
	<li>against the law and the student code of conduct.</li>
	<li>taken seriously by Virginia Tech. University employees (except those at the Cook Counseling Center or Schiffert Health Center)
		are required to report disclosures of student sexual assaults to the Title IX coordinator or designee.</li>
</ul>
<p>The
	<a href="http://www.womenscenter.vt.edu/" target="_blank">Women’s Center at Virginia Tech</a> and Cook Counseling Center are dedicated to creating safe environments for a victim
	to discuss any trauma that may have impacted their lives because of sexual assault.</p>
<p>If you have been sexually assaulted, you may find it helpful to talk with a Cook Counseling Center counselor. If you feel
	anxious, frightened, depressed or angry, tearful, restless or irritated, a counselor can help you work through your feelings
	about the assault.</p>
<h3>If You have Recently Become a Victim of Sexual Assault</h3>
<p>If you have recently been raped or sexually assaulted, it is important for you to seek help immediately. Go to a safe place
	where you can contact someone who can help you, such as a close friend, the Women’s Center at Virginia Tech at 540-231-7806
	(Mon. – Fri. 8 a.m. to 5 p.m.) or the
	<a href="http://www.wrcnrv.org/" target="_blank">Women’s Resource Center of the New River Valley</a> at 540-639-1123 (24-hours a day) for support.</p>
<p>You may need medical attention for injuries you received during the assault. You can receive medical services by calling
	Schiffert Health Center at 540-231-6444. If possible, do not change clothes, shower, bathe, or brush your teeth. If you
	do change clothes and shower, place clothing in a paper bag and take them with you to Schiffert Health Center or a hospital.</p>
<p>For more information on what to do within the first 72 hours of a sexual assault, visit the
	<a href="http://www.womenscenter.vt.edu/"
	 target="_blank">Women’s Center at Virginia Tech</a>.</p>