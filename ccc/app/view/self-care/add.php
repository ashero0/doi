			<h5 class="mb-0">
				<button class="btn btn-link accordion-button" data-toggle="collapse" data-target="#collapseFive" aria-expanded="false" aria-controls="collapseFive">
					Success Strategies for College ADD Students
				</button>
			</h5>
		</div>
		<div id="collapseFive" class="collapse" aria-labelledby="headingFive" data-parent="#accordion">
			<div class="card-body">
				<p class="font-weight-bold">Successful Transition Strategies</p>
				<ul>
					<li>Carefully plan your first semester by choosing courses that maximize your strengths.</li>
					<li>Use available academic support services such as peer tutors.
						<b>Do not wait until you are failing to seek help.</b>
					</li>
					<li>Remember that the typical ADHD student requires three times as much study time as the average student.</li>
					<li>As an ADHD student, plan on spending more than four years to successfully complete your bachelor's degree.</li>
					<li>Avoid drinking alcohol. Alcohol can impair academic functioning in any student. However, studies have shown that alcohol
						negatively affects ADHD students at twice the rate of the average student.</li>
				</ul>

				<p class="font-weight-bold">Class Scheduling Strategies</p>
				<ul>
					<li>Try to schedule classes at times when you feel you are most alert.</li>
					<li>Do not take more than two quantitative classes in a day.</li>
					<li>Do not take more than two back-to-back classes in a semester.</li>
					<li>Avoid taking several classes with especially demanding reading or writing requirements during a single semester.</li>
					<li>Do not take more than twelve hours per semester.</li>
					<li>Be aware of drop/add dates. Consider dropping a class if a professor is not willing to accommodate you.</li>
					<li>If possible, plan on taking especially difficult classes on a pass/fail basis</li>
				</ul>

				<p class="font-weight-bold">In-class Strategies</p>
				<ul>
					<li>Do not sit by the window.</li>
					<li>Tape record class lectures when possible or use your professor’s notes.</li>
					<li>Participate in class discussions as much as possible to enhance your concentration.</li>
					<li>Try to schedule the timing of medication doses to maximize your class performance.</li>
					<li>When unsure of the meaning of exam questions, ask the professor. Inform the professor of this possibility prior to your
						exam.
					</li>
				</ul>

				<p class="font-weight-bold">Effective Study Strategies</p>
				<ul>
					<li>Study in a distraction-free environment – you should not study in your room.</li>
					<li>Study in one-hour blocks. Do not expect to be able to study for extended periods of time.</li>
					<li>Study with others to reinforce your understanding of the material.</li>
					<li>Know your learning style – auditory, visual, or kinesthetic – to maximize learning; take this information into consideration
						when working with a tutor.</li>
					<li>When you study alone, dictate key points into a tape recorder to enhance learning.</li>
					<li>If you are an auditory learner, read aloud to yourself while studying.</li>
					<li>Always "over-learn" the material. Students with memory or attention problems need to practice or rehearse more than
						the average student.</li>
					<li>Use active learning techniques, such as SQ3R, that allow you to do the following:
					</li>
					<ul>
						<li>Skim a chapter to determine both content and important points.</li>
						<li>Change chapter headings into questions.</li>
						<li>Read the text to find answers to your questions.</li>
						<li>Recite answers to yourself.</li>
						<li>Review the major points. Read each heading and write down the main points you can recall.</li>
					</ul>
				</ul>

				<p class="font-weight-bold">Organization and Time Management Strategies</p>
				<p>Managing time is a major problem for college ADHD students because of the considerable amount of unstructured time they
					have in college. The college ADHD student will need to devote more time to studying than the average college student.</p>
				<ul>
					<li>Learn to use a daily planner.</li>
					<ul>
						<li>Put all due dates and exams in the planner.</li>
						<li>Break reading assignments into little bits and schedule times to do the reading.</li>
						<li>Include all your social, work, and recreational activities in your planner.</li>
						<li>Always have your planner with you.</li>
					</ul>
					<li>Allocate 10-15 minutes in the morning, or before you go to bed at night, to plan your day.</li>
					<li>Make a "To Do" list and keep it in your planner.</li>
					<li>Make constructive use of your time between classes, e.g., catching up on assignments, doing assigned reading, etc.</li>
				</ul>

