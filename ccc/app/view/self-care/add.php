		</div>
		<div id="collapseThree" class="collapse" aria-labelledby="headingThree" data-parent="#accordion">
			<div class="card-body">
				<p>There are three main characteristics of ADD:</p>
				<p class="font-weight-bold">Inattention</p>
				<ul>
					<li>Difficulty organizing tasks</li>
					<li>Difficulty staying on task (quick loss of interest) and maintaining effort</li>
					<li>Difficulty with transitions or prioritizing tasks, following instructions, and completing school work</li>
					<li>Problems with misplacing things needed for tasks</li>
					<li>Becomes easily distracted by extraneous stimuli</li>
					<li>Difficulty remembering daily activities</li>
				</ul>

				<p class="font-weight-bold">Hyperactivity</p>
				<ul>
					<li>Experiences minor motor restlessness, such as fidgeting of hands</li>
					<li>Difficulty remaining seated and talks excessively</li>
					<li>Difficulty regulating restlessness to situational demands</li>
					<li>Difficulty channeling physical restlessness in productive directions</li>
					<li>Difficulty listening to others</li>
					<li>Becomes easily distracted while reading</li>
				</ul>
				<p class="font-weight-bold">Impulsiveness</p>
				<ul>
					<li>Speaks or acts without considering the consequence</li>
					<li>Difficulty taking turns</li>
					<li>Feelings of being out-of-control, which can result in obsessive compulsive behaviors</li>
					<li>Has need for high stimulus activity</li>
				</ul>
			</div>
		</div>
	</div>

