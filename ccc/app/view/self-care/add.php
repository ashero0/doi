		<div id="collapseSix" class="collapse" aria-labelledby="headingSix" data-parent="#accordion">
			<div class="card-body">
				<p class="font-weight-bold">Coping with Hyperactivity</p>
				<ul>
					<li>Consider a job that enables you to move around more regularly. Avoid desk jobs that require long meetings.</li>
					<li>If employed in a desk job, take frequent breaks – every 20-30 minutes or so.</li>
					<li>Engage in some regular physical activity after you get off work.</li>
					<li>If you have to attend a long meeting, bring something you can do inconspicuously with your hands during the meeting.</li>
					<li>When being considered for a position that would not allow for much physical movement, consider your ability to successfully
						cope before accepting.</li>
				</ul>

				<p class="font-weight-bold">Coping with Distraction</p>
				<ul>
					<li>Try to schedule work periods during which you will not be interrupted. Inform your supervisor and subordinates of the
						importance of this to your job performance.</li>
					<li>Schedule intensive work in short, time-limited chunks.</li>
					<li>If you are distracted by a rapid flow of ideas tangentially related to a current project or other projects, write the
						intruding thoughts down or record them on a voice recorder in a "brainstorming log."</li>
					<li>When making meeting presentations, use an outline of what you must say and stick to it.</li>
					<li>If you become distracted by your own ideas while listening to others speak at meetings, write down your ideas as you
						listen.
					</li>
					<li>Keep your workspace orderly. Do not, however, use desk organization as a distraction from tasks that must be done.</li>
					<li>If you become distracted by returning phone calls, opening mail etc., train yourself to avoid these diversions and keep
						on task.</li>
				</ul>

				<p class="font-weight-bold">Staying Organized</p>
				<ul>
					<li>If you have a problem with staying organized and following-through, work with someone for whom these are strengths.</li>
					<li>Jobs that involve many short-term projects are more compatible with adult ADHD than longer-term projects.</li>
					<li>If your job involves receiving tasks from several different people, have your direct supervisor "filter" your work assignments
						before they get to you.</li>
				</ul>

				<p class="font-weight-bold">Managing Time on the Job</p>
				<ul>
					<li>Pro-actively plan activities instead of reacting to events, impulses, or moods.</li>
					<li>Prioritize activities on your "To Do" list.</li>
					<li>Avoid over-scheduling your day.</li>
					<li>Break large projects into small manageable parts. Set deadlines for each part.</li>
					<li>Have your planner with you at all times. If unable to start a new task right away, put it on your "To Do" list.</li>
					<li>Do not say "yes" when you mean "no." When asked to do something, do not respond impulsively; instead, learn to check
						on previous commitments before responding.</li>
					<li>Learn to say "no" to last-minute impulses, unless they are genuine emergencies. ADD adults are chronically late due
						to impulses to handle "brief" last-minute tasks on their way out the door.</li>
					<li>Avoid getting caught in hallway conversations when on your way to important meetings.</li>
					<li>Leave early for meetings and take some work with you, should there be any "down-time" during the meeting.</li>
					<li>Phone calls may take more time than you expect, so learn to end calls a few minutes early. This leaves more time for
						planned tasks.</li>

				</ul>

				<p class="font-weight-bold">Memory</p>
				<ul>
					<li>Record the audio for important meetings and seminars. Be sure to set track markers to help you locate important aspects
						of the session.</li>
					<li>Take notes at meetings, even though you may also be recording them.</li>
					<li>Take notes whenever you meet with your supervisor or a co-worker; never rely on memory alone.</li>
					<li>To double-check accuracy, always send copies of your interpretation of agreements or commitments to the individuals
						involved.</li>
				</ul>

