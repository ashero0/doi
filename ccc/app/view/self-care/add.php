				<p>People with Attention Deficit Hyperactivity Disorder (ADHD) tend to be of average or above-average intelligence. The
					ADHD diagnosis includes individuals whose symptoms are primarily hyperactive (ADHD-H), inattentive (ADHD-I), or both
					(ADHD-C). About three to five percent of the general population has ADHD, while approximately one to three percent of
					college students are diagnosed with ADHD. The condition is diagnosed about three times more frequently in males than
					in females.</p>
