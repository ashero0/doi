<h1>Attention Deficit Disorder (ADD)</h1>
<p>Attention deficit hyperactivity disorder (ADHD) is a brain-based disorder that affects all aspects of one’s life. ADHD is
	characterized by inattention and/or hyperactivity, which is more frequent and severe than what is typically observed in
	individuals at a comparable level of development. Those who exhibit extreme hyperactivity are commonly diagnosed with attention
	deficit hyperactive disorder (ADHD-H). Those with primarily inattentive symptoms are diagnosed as ADHD-I. Individual with
	both clusters of symptoms are diagnosed with ADHD-C. Unless properly treated, ADHD could interfere with one’s academic achievement,
	self-esteem, and professional and personal relationships.</p>
<p>A good understanding of the condition is essential in coping with an ADHD diagnosis. Educate yourself by talking to health
	care professionals and to others who have ADHD, as well as by using other resources such as published books and pamphlets.</p>

