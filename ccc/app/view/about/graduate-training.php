<h1>Graduate Training</h1>
<p>In keeping with the educational mission of the university, Cook Counseling Center is committed to the practical training
	of mental health professionals as they complete their academic programs and pursue professional licensure. The experiences
	provided at Cook Counseling Center are intended to provide the graduate student with skills and knowledge relevant to generalist
	clinical practice, with specialty expertise in working with a college population. Below is a list of Graduate training available
	through Cook Counseling Center:</p>
<h3>
	<a href="<?=BASE_URL?>about/graduate-training/practicum"><u>Graduate Practicum (Externship)</u></a>
</h3>
<p>This training is intended for clinical psychology, counseling psychology, counselor education, or clinical social work graduate
	students. Students are generally in their second or third years of doctoral training, or are advanced master's students.</p>
<h3>
	<a href="<?=BASE_URL?>about/graduate-training/doctoral-internship"><u>Doctoral Internship in Professional Psychology</u></a>
</h3>
<p> This training is intended for clinical or counseling psychology doctoral graduate students who have completed all of the
	academic requirements of their doctoral training, including the completion of all coursework and passing of their departmental
	comprehensive exam, except for dissertation. </p>
<h3>
	<a href="<?=BASE_URL?>about/graduate-training/vcom-clerkship"><u>VCOM Medical Student Clerkship</u></a>
</h3>
<p> Third and fourth year students from the Edward Via College of Osteopathic Medicine and the Virginia Tech Carilion School
	are able to occasionally connect to a clinician as their preceptor, sitting in on psychiatric evaluations and counseling
	sessions with the permission of the student who is being seen. </p>