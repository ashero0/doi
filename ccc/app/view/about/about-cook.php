		<p>
			Thomas E. Cook Counseling Center provides opportunities for students to learn more about themselves as individuals, form
			deeper relationships with their peers, and grow to benefit our community and society at large. Cook Counseling Center
			staff provides these learning opportunities through individual and group counseling, psychiatric care and psychotherapy,
			and educational programming.
		</p>
		<p>
			Similarly, the staff also provides support to the Virginia Tech community through cooperative programming and consultation.
			They are guided by the highest professional and ethical standards of their professions and are dedicated to providing
			quality services to the student population.
		</p>
		<p>
			Cook Counseling Center supports the academic mission of the university through the enhancement of student learning and retention.
			Cook Counseling Center is committed to the development of the mental healthcare profession while providing training opportunities
			to graduate students, predoctoral interns and postdoctoral residents.
		</p>
