<p>If a student has a significant psychiatric or psychological problem that has substantially interfered with
    their ability to meet academic responsibilities, the student may be eligible to apply for academic relief.
    Through the academic relief process, students may drop a class or an entire semester’s classes.We understand
    that students may be involved in many situations that are stressful or distracting. These can include a
    death in the family, ending a significant relationship or anxiety about family problems or major life
    decisions. On many occasions these can interfere with studying and academic efficiency, leading students to
    seek counseling. However, these issues do not guarantee that a student will qualify for academic relief.
    Students are expected to use their nine hours of course withdrawals in these situations.In order to qualify
    for a recommendation, students <b>must</b> have documentation showing that he or she has a significant
    psychiatric or psychological problem that has substantially interfered with meeting their
    academic responsibilities.</p>

<h3>Possible justifications for academic relief include:</h3>
<ul>
    <li>Extended illness with evidence of psychiatric or psychological treatment on or off campus</li>
    <li>Periods of hospitalization for psychiatric treatment</li>
    <li>Evidence of psychiatric illness with related treatment by a qualified health professional</li>
</ul>

<p>Receiving academic relief may have consequences in other aspects of students’ lives including, but not limited to,
    residential life on campus, financial aid and health insurance. Please contact the appropriate department to discuss
    these implications.</p>

<h3>Important Note about Academic Relief:</h3>
<p>Students who withdraw from <b>all or 50% or more</b> of the hours attempted in a semester will be required to "sit
    out" (not benrolled at Virginia Tech) for the following semester. After engaging in treatment targeting the
    conditions that created the academic difficulties (at least <b>5 sessions</b>), they may submit an application for
    re-enrollment. A recommendation from a health or mental health provider must be submitted to the Director of Cook
    Counseling Center for permission re-enroll in classes at Virginia Tech.<p>