<h1>Writing Papers</h1>
<br>
<p>Students often put off a written assignment, considering it a chore too formidable to approach until the last minute.
    As a result, grades inevitably suffer. Writing is not a talent reserved for a select few, it is a skill that can be
    learned. Planning and organization are its essentials. Students can improve their writing abilities through learning
    about these tools and putting effort into practicing them.</p>

<p>Below is a guide to organized writing. Use this outline in writing class assignments, test essays, and term papers.
</p>

<h4>Introduction – Opening Paragraph</h4>
<ul>
    <li>Begin with a general statement.</li>
    <li>Narrow it down to the controlling idea (for thesis statement).</li>
</ul>
<h4>Body – Three Developing Paragraphs</h4>

<p>In each paragraph:</p>
<ul>
    <li>Use transitions (repetition of key words and ideas) to connect paragraphs together.</li>
    <li>Develop the topic sentence with details, definitions, illustrations, comparisons, and contrasts.</li>
    <li>Conclude the paragraph with a summary of the main idea.</li>
</ul>
<h4>Conclusion – Finishing Paragraph</h4>
<ul>
    <li>Restate the thesis.</li>
    <li>End with a general statement finalizing the discussion.</li>
</ul>
