<h1>Time Management</h1>
<br>
<p>Being successful at the university level will requires a careful and effective utilization of time. Students are
    typically scheduled for 15 or more hours of classroom work per week. Additionally, they are expected to average two
    to three hours of preparation for each hour in the classroom. This means that at least a 45-hour workweek is
    involved in being a full-time student. On top of that, many students have part-time jobs, family, and social
    responsibilities.</p>

<p>The job of being a university student can be carried out efficiently or inefficiently. The way we use our time (or
    waste it) is largely a matter of habit patterns. One of the best techniques for developing more efficient habits for
    the use of time is to prepare a time schedule. Studies have shown that people who have a well-designed schedule
    achieve a greater amount of success than those who donâ€™t.The most successful system for most students is to combine
    long and short-range planning. Doing so, a student can make a general schedule for an entire semester and then
    create a more specific plan for several days at a time.</p>

