<h3>Concentration Basics</h3>
<p>There are two areas that are crucial to improving concentration: the study environment and structure for study
    sessions. These areas are broken up into several conditions. It is not necessary to fully perfect all of these
    conditions, but the more of them you implement, the more you are likely to be able to improve your ability to
    concentrate.</p>
<b>Study Environment</b>
<ul>
    <li>Distraction free</li>
    <li>Conditions that are conducive to studying (i.e. chair, desk, temperature, and lighting)</li>
    <li>Materials needed for studying bring present</li>
</ul>
<b>Structure for Study Sessions</b>
<ul>
    <li>Develop realistic goals for the study session</li>
    <li>Decide on the order that tasks will be completed</li>
    <li>Plan a reward</li>
</ul>

