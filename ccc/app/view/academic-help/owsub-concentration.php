                <b>Distraction free</b>
                <p>Much study time is wasted by distractions like talking to friends, playing computer games, or
                    watching TV. Think about your last study period. How long did you try to study (time from beginning
                    to end)? How much of that time was actually spent studying? If there is a big discrepancy in your
