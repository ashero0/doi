                <b>Regularly plan time for review</b>
                <p>It's actually better if review methods vary. For instance, you can review your notes silently, read
                    them aloud and listen to yourself, write down main points from your notes, create ways to apply the
                    information, test yourself on your notes, or teach the information to a friend. Using various
                    methods for review will help you gain a deeper understanding of the material. More active (versus
                    passive) review methods will also help you retain the information more fully.</p>
                <b>If you can’t remember something, brainstorm</b>
                <p>Not being able to recall information is a part of the learning process. The trick is to create
                    connections between the information you are trying to learn and the information you already know.
                    So, if you are unable to remember something, brainstorm the information you do know that is related
                    to the topic. If two concepts are similar and you remember information about one of them, recall
                    this information and you may find that you are able to recall the information about the other. In
                    the learning process, try to create examples to help you recall information. If the professor used
                    examples in class, recall his/her examples. Making connections between the material you know and the
                    material you are trying to learn will help you recall the less familiar material when you need it.
                </p>
