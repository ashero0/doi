                    distractions may be personal concerns, which can be more difficult to control. If you find that this
                    is your greatest study distraction, you may wish to talk with a counselor on campus to try to find
                    ways to reduce or resolve your personal concerns.</p>
                <b>Conditions that are conducive to studying (i.e. chair, desk, temperature, and lighting)</b>
                <p>The chair used for studying should be comfortable enough that you can sit for 45 - 50 minutes at a
                    time. Sitting at a desk or table that provides adequate space for your materials to be spread out is
                    important. The lighting in the room should not cause eyestrain and the temperature should be
                    comfortably cool.</p>
                <b>Materials needed for studying being present</b>
                <p>It's important to make sure that you have everything that you will need for a particular study
                    session present when you begin. If you're going outside your room to study, think through what you
                    will need with you to accomplish your study goals and take it with you.</p>
