<h3>Memory Basics</h3>
<p>Creating conditions for better concentration is likely to improve the quality of your studying; however, it is also
    necessary to implement skills that will enhance your ability to remember the material you are studying.</p>
<b>Four skills that can improve memory:</b>
<ul>
    <li>Organizational skills</li>
    <li>Using your body</li>
    <li>Using your brain</li>
    <li>Including time for recall</li>
</ul>

