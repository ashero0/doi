            </h5>
        </div>
        <div id="collapseOne" class="collapse" aria-labelledby="headingOne" data-parent="#accordion">
            <div class="card-body">
                <b>Learn main ideas first, then details</b>
                <p>At the beginning of the semester, preview each chapter of your textbook to get an idea about
                    what you will be learning in the class. Prior to reading each chapter, preview that chapter
                    to gain an understanding of the topics that it will cover and what you will be expected to
                    learn. This will provide a framework for understanding the details you will learn as you
                    read the chapter.</p>
                <b>Make the information relevant</b>
                <p>Try to make connections between what you are learning in class and your everyday life or
                    future career. Making the information meaningful to you will make the information easier to
                    learn and remember.</p>

                <p>Connect the information you already know with new information. Creating associations also
                    makes information easier to learn and remember. Information is stored in our brains
                    according to a scheme. Creating these associations helps to fit the new information into the
                    schemes that already exist in your brain.</p>
            </div>
        </div>
    </div>
    <div class="card">
        <div class="card-header" id="headingTwo">
            <h5 class="mb-0">
                <button class="btn btn-link accordion-button" data-toggle="collapse" data-target="#collapseTwo"
                    aria-expanded="false" aria-controls="collapseTwo">
                    Improve Memory by Making it Physical
                </button>
            </h5>
        </div>
        <div id="collapseTwo" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
            <div class="card-body">
                <b>Use your body and your senses to help build memory</b>
                <p>It has been said, "people remember 90 percent of what they do, 75 percent of what they see, and 20
                    percent of what they hear" (D. Ellis, 1997, Becoming A Master Student, 8th ed.). Learning actively
                    can involve your whole body. You can stand up and talk aloud as you study, using your arms, legs,
                    eyes, ears, and voice. Getting your body involved puts energy into the study process and makes it
                    less boring. Maybe you have already used this strategy when preparing for a speech; however, it can
                    work for learning other materials as well.</p>
                <b>Relax</b>
                <p>Have you ever been unable to recall information in a test situation, but an hour later could remember
                    the information with clarity? This may suggest that you were not relaxed during the test. Relaxation
                    allows more blood to flow to the brain and thus allows us to think more clearly. Our brains are
                    alert when we are relaxed and this will enable us to perform more effectively and efficiently.</p>
                <b>Use visualization</b>
                <p>The more visual you can make the learning process, the easier it will be to recall the information.
                    Be creative and use your imagination. Create mental images that you can associate with the
                    information you are trying to learn.</p>
            </div>
        </div>
    </div>
    <div class="card">
        <div class="card-header" id="headingThree">
            <h5 class="mb-0">
                <button class="btn btn-link accordion-button" data-toggle="collapse" data-target="#collapseThree"
                    aria-expanded="false" aria-controls="collapseThree">
                    Improve Memory by Making it Cognitive
                </button>
            </h5>
        </div>
        <div id="collapseThree" class="collapse" aria-labelledby="headingThree" data-parent="#accordion">
            <div class="card-body">
                <b>Overlearn the material</b>
                <p>Short-term memory is not very effective in test situations. Our short-term memories have limited
                    capacities and do not respond well under stress. Long-term memory is much more reliable in stressful
                    situations and long-term memory has an unlimited capacity. Moving information from short-term to
                    long-term memory requires time, energy, understanding, and repetition. When you are studying
                    material, study it multiple times using different study techniques. Also, vary the order in which
                    you study the material. The "recency effect" says that we remember what we heard or learned to last
                    the best. The "primacy effect" says that we remember what we heard or learned first the best. If you
                    always study material in the same order, it is likely that you will be unable to remember the
                    information covered during the middle of study sessions, so it is important to study this
                    information first or last during different sessions.</p>
                <b>Adjust your attitude about learning</b>
                <p>If you find a subject boring or uninteresting, it will be harder to learn. If you are taking flying
                    lessons to become a pilot, you will tend to pay close attention to the instructor because learning
                    the material is very important for your safety. While classes do not present the same life or death
                    concerns, try to have a positive attitude about your classes – it will enhance your ability to
                    recall the information. If you believe something is going to difficult and think that you will never
                    learn it, it probably will be difficult to learn the material. Wanting to learn the material and
                    believing that you can, will contribute to the ease with which the material is learned.</p>
                <b>Practice multiple study techniques for each class</b>
                <p>Use a variety of study techniques to learn the material. If you are trying to learn how to solve
                    math or engineering problems, do the problem, talk yourself through the problem to ensure that
                    you understand how you got from Step A to Step B to Step C, and then review the process with a
                    friend. If you are trying to learn material from a chapter in a sociology book for a test, use
                    SQ3R to read the chapter, review your class notes periodically, write new terms on note cards
                    that you carry with you, and review when you have a few idle minutes. There are many techniques
                    that can be used for different classes. Using the same techniques is probably not the most
                    useful strategy for all of your classes, so it is important to decide what techniques to employ,
                    given the type of material you must learn for each class.</p>
            </div>
        </div>
