                <b>Develop realistic goals for the study session</b>
                <p>Having a specific, realistic goal for all study sessions is essential. Expecting to accomplish too
                    much in the time you have leads to discouragement. Setting, and achieving, goals leads to a sense of
                    accomplishment. This is also critical for reducing stress and meeting deadlines. Underestimating how
                    long a task will take is likely to result in having less time to do another task, which causes
                    stress.</p>
                <b>Decide on the order that tasks will be completed</b>
                <p>This makes your study plan more specific. It's generally best to begin with difficult or boring
                    subjects. Your ability to concentrate will be highest when you begin studying and the task is more
                    likely to take less time if you are studying at your “best time of the day.” It's easier to find the
                    motivation to do things you like to do, even when you are tired or have less energy.</p>
                <b>Plan a reward</b>
                <p>Planning to reward yourself when you have finished your study session can be very effective for
                    helping you accomplish the tasks you need to do, but only if you use restraint and don't allow
                    yourself the reward until you've finished your studying for the day. Rewards might be things like
                    watching your favorite TV show, talking with a friend, going to a movie, or anything else you enjoy
                    doing. Make a list of the things you could use as rewards so you don't have to spend time thinking
                    about your reward each day when you are planning your study period.</p>
