<h1>Improving Concentration / Memory Workshop</h1>
<br>
<p>Concentration and memory work together, but one does not lead to the other. To concentrate is to direct your mental
    powers, or efforts, towards a particular activity, subject, or problem. Memory is the ability to remember
    information, experiences, and people. There are some specific skills that can be learned to enhance both
    concentration and memory. Practicing these skills is likely to improve one's success as a student.</p>

<p>Once something is stored in our brains, we never forget it. We may, however, have difficulty recalling the
    information. It is also possible that the information we are trying to recall was never stored. This program will
    help you understand the structure of memory and also suggest some skills you can use to try to improve your memory.
</p>

<p>Good concentration will enhance memory. If we only practice skills that improve our memory but never look at factors
    that enhance concentration, our efforts will only be marginally successful. For this reason, tips for improving
    concentration will also be discussed.</p>

<p>Improving memory and concentration can be challenging. If you want further information about strategies to use to
    improve these skills, review other sections of Academic Support for Students. If you have been trying to improve
    your memory and concentration but the strategies you're trying just don't seem to be helping as much as you would
    like, please contact <a href="<?=BASE_URL?>about/contact-us" target="_blank">Cook Counseling Center</a>.</p>

