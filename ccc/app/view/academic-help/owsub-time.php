                <p>This should only take a few minutes because you can use your weekly schedule to create it quickly.
                    Write your daily schedule on an index card or a daily planner. Carry your schedule with you so you
                    can refer to it as needed and cross items off once they are completed. This last step provides a
                    sense of accomplishment.</p>
                <b>As you write out your daily schedule, assess your priorities</b>
                <p>Some activities must be done on a particular day while others may be optional for that day. You can
                    use the A, B, C system of prioritizing your tasks: A tasks must be done that day and C tasks are
                    optional, while B tasks are important but not as important as A's. Try to accomplish all of your A
                    tasks before moving on to the B tasks and finally the C tasks. This can reduce your stress level.
                </p>
