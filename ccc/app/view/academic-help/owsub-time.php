                <b>Record known class assignments including quizzes, tests, projects, and papers</b>
                <p>Recording your class assignments at the beginning of the semester creates a framework for your
                    semester schedule. It lets you know when you are likely to have high academic demands and when you
                    will have more flexibility for scheduling recreational activities.</p>
                <b>Record co-curricular activities, including work hours, meetings, social commitments, and out-of-town
                    weekends</b>
                <p>Recording co-curricular activities allows you to have a more accurate picture of how full or open
                    your schedule will be throughout the semester. These activities are important for providing balance
                    in your schedule.</p>
                <b>Remember</b>
                <p>It is important to update your semester schedule regularly. Assignment due dates change, assignments
                    are added, and activities are planned. Keeping an accurate semester schedule facilitates the next
                    step of this process: Assessing and Planning your Weekly Schedule.</p>
