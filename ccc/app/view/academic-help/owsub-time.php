<h1>Time Management Strategies Workshop</h1>
<br>
<p>In many cases, new students may not have had to manage their time efficiently in high school to ensure good grades.
    Whether this is because of being bright or not being challenged enough, these students are faced with a significant
    change when they get to Virginia Tech. Some students who received A's and B's in high school now receive C's and D's
    in college. Those receiving lower grades are probably no less capable than those receiving higher grades, but often
    their study skills, including time management, are less effective.</p>

<p>If you can identify with any part of the above paragraph, working on improving your time management may be beneficial
    to you and your grades.</p>

<p>On this page, you will be given the opportunity to assess where your time goes and make decisions about changes you
    would like to make that will help you use your time more effectively. There is no one right way to manage your time,
    however, it is important to get to know yourself so that you can make good decisions about how to use your time. We
    all have 168 hours in a week to use as we wish, but some people make better use of this time than others.</p>

<p>Developing good time management habits takes time and requires hard work. If you want further information about time
    management, you may wish to review other pages within Academic Support for Students related to time management. If
    you have been trying to improve your time management but the strategies you're trying just don't seem to be helping
    as much as you would like, please contact Cook Counseling Center.</p>

