            <div class="card-body">
                <b>Make a list of what you have to accomplish during the upcoming week, including class assignments and
                    class attendance</b>
                <p>Being as inclusive as possible in your list of schoolwork that must be done for the week is essential
                    to making your schedule work. Everything takes take time, whether it's reading a chapter, working
                    problems, or writing an outline for a research paper.</p>
                <b>Include co-curricular activities, work hours, errands, exercise, meals and time with friends on your
                    list of things to do for the week</b>
                <p>Daily living activities and co-curricular activities are important and provide balance in your
                    schedule, but take time away from studying. Preparing dinner and cleaning up afterwards or attending
                    a student organization meeting can take as much time as reading a chapter in a textbook.</p>
                <b>Estimate how long each task will take</b>
                <p>This is an essential, but often-overlooked step in the time scheduling process. Activities take
                    different amounts of time, so, to effectively use your time, it is important to estimate how long a
                    task will take. It's better to estimate conservatively if you don't know how long something will
                    take. If you finish 30 minutes or an hour early, you can use that time however you would like, but,
                    if you haven't allowed enough time, you'll have to take time away from another task to complete the
                    one that is taking longer than planned.</p>
                <b>Identify the day that you will accomplish each task, keeping in mind the amount of time the task will
                    take and other things you must also do that day</b>
                <p>This facilitates the next step of this process: Making a Daily Schedule. By looking at your whole
                    week and realizing everything you need to accomplish during that time, you are more likely to avoid
                    missing deadlines. You can make adjustments throughout the week instead of suddenly finding that you
                    have a six-hour task with only three hours remaining before the deadline. Making your schedule for
                    the next week is a good activity for Friday afternoon or evening, before beginning your weekend.
                    Weekends provide the largest blocks of time for study, so if you will have a very full week ahead,
                    it may be helpful to complete some of the tasks on the weekend to decrease the time crunch during
                    the coming week.</p>
