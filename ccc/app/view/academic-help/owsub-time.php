                <p>Here are some strategies that you may find helpful if your schedule is not working as efficiently or
                    effectively as you would like. When trying any new strategy, it is important to practice it
                    regularly and to practice it long enough so that you have a way of evaluating whether or not it is
                    helping. Tests are good ways to evaluate new study strategies. If you take a test and don’t perform
                    as well we you would have liked to, try a new strategy until you receive the results of the next
                    test you take to get an idea on whether the new strategy is working for you.</p>
                <b>Identify your most productive time of day</b>
                <p>Studying at the time of day when you feel best (whether that is morning, afternoon, or early evening)
                    will enable you to complete your assignments in less time. Research studies show that what we can
                    accomplish in 60 minutes when we're less fatigued will take as much as 90 minutes to accomplish when
                    we are more fatigued.</p>
                <b>Study difficult or boring subjects first</b>
                <p>Study subjects that are more of a challenge for you first when you are less fatigued. Save subjects
                    you like to study for later, when you are feeling more tired, but need to continue to study to keep
                    up with your work. It will be easier to find the motivation to study something you find enjoyable
                    when you are tired than for a subject you dread.</p>
                <b>Study in the same place every time</b>
                <p>Studying in the same place each day is like going to class in the same room. You begin to associate a
                    particular activity with a particular location so when you are in that location, you are able to
                    focus on the task at hand more quickly. Studying on your bed or in your bedroom is not advised
                    because you probably associate your bed and bedroom with sleeping, not studying. It's too easy to
                    take those 10-minute naps that turn into two-hour naps.</p>
                <b>Use the library</b>
                <p>Libraries are good places to study because studying is one of the only activities that people do in
                    that environment. If there are reasons you choose not to use the library, try to find another
                    location outside of your room that provides a good study environment and is relatively free of
                    distractions.</p>
                <b>Avoid distractions</b>
                <p>Many things can provide a distraction from studying if you are looking for ways to procrastinate.
                    Previously on this page you identified your top five time wasters. For the next few weeks, try to
                    find ways to reduce the frequency with which these distracters are interfering with your study time.
                    This might mean that finding another place to study would be helpful.</p>
                <b>Use waiting time</b>
                <p>If you use public transportation to commute to and from campus, there is probably some waiting time
                    involved. This is a great time to study pieces of information such as learning vocabulary for a
                    foreign language class or memorizing a chemical reaction sequence. Write this information on note
                    cards and carry them with you so you can study during your waiting time.</p>
                <b>Treat school like a full-time job</b>
                <p>Try to accomplish as many of your school tasks as possible within a concentrated period of time, such
                    as 8:00 a.m. – 5:00 p.m., Monday through Friday. If you use these hours for attending class or
                    studying, you'll have much more free time to spend with friends in the evenings and on weekends.
                    Your classes are likely to get the amount of attention they require as well. This doesn't mean that
                    you'll never need to study in the evenings or on the weekends – there will still be crunch times
                    when you will need to do more studying. However, treating school as a full-time job, and adopting
                    the hours of a full-time job, will probably result in better, more efficient management of your
                    time.</p>
