        <div id="collapseFour" class="collapse" aria-labelledby="headingFour" data-parent="#accordion">
            <div class="card-body">
                <p>Reciting material as you go retrains your mind to concentrate and learn as you read. When you can
                    answer your questions about the section that you’re reading, move to the next section and repeat the
                    question, read, recite process again. Use this for every section in the chapter</p>
                <b>When do you recite?</b>
                <p>At the end of each section.</p>
                <b>How do you recite?</b>
                <p>Look at the question(s) you wrote down before you read the section. Cover your answers with a piece
                    of paper and see if you can answer the questions from memory.</p>
                <b>What if you can’t recall the answers to your questions?</b>
                <p>Reread the section, or the part of the section, that has to do with that question.</p>
            </div>
