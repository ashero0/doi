            <div class="card-body">
                <p>No technique is useful 100 percent of the time. Now that you’ve learned another study strategy, it is
                    important to decide when to use it. Different study strategies work best in different situations.
                </p>
                <b>When should you use SQ3R?</b>
                <p>SQ3R is an excellent technique to use with textbooks that provide a lot of information and require
                    you to learn the material in depth. Textbooks in many disciplines such as biology, psychology, and
                    sociology fall into this category.</p>
                <b>SQ3R is useful with many textbooks – what textbooks is it less likely to be useful for?</b>
                <p>SQ3R is probably less useful with textbooks that focus on helping you solve problems, like with math
                    textbooks. In this case, the main emphasis of reading the chapter is on helping you solve math
                    equations. Focusing your energy on solving mathematical problems using the information in the
                    chapter is probably a better use of your time.</p>
                <p>SQ3R may be less useful for two other types of textbooks: beginning foreign language texts or texts
                    for English class. The focus of beginning foreign language texts is often vocabulary, verb tense,
                    and sentence construction. Books for English class may be novels, and the purpose of reading may be
                    focused more on the "big picture," rather than on the content of a particular section or chapter.
                </p>
                <b>Does it matter how the professor uses the textbook in class?</b>
                <p>If your professor is reviewing a lot of material from the textbook during class lectures, it is
                    probably well worth your time to read the chapters using the SQ3R method.</p>
                <p>If your professor uses the textbook as a supplement or reference to class lectures, you will need to
                    balance spending your study time solving problems, reviewing notes, or doing other class homework
                    assignments to fill the time that would be spent using SQ3R.</p>
