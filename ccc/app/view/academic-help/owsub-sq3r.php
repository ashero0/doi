        <div id="collapseTwo" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
            <div class="card-body">
                <p>Now that you have surveyed the entire chapter and built a framework for understanding, it is time to
                    begin the reading process. This step and the next two, reading and reciting, are repeated over and
                    over as you read the chapter.</p>
                <b>Turn boldface headings into one or more questions. Write those questions on the left third of a piece
                    of paper.</b>
                <p>As you read this section, you will be looking for the answer to your questions. For example, if you
                    are reading a book to help you improve your study skills and the heading is "Use a Regular Study
                    Area," the questions you might ask are, "Why should I have a regular study area?" and "Where should
                    my regular study area be located?"</p>
                <b>Why is this step necessary?</b>
                <p>When your mind is actively searching for answers to questions, it becomes engaged in the learning
                    process. This will help you remember and understand the information.</p>
            </div>
        </div>
    </div>
