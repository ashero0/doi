                <p>Now that you've learned about SQ3R, the next step is to incorporate it into your study strategies.
                    You may want to begin by selecting one class for which you will use the SQ3R method to read every
                    chapter prior to your next test. Here are some additional strategies you may want to implement along
                    with reading the chapter using this method:</p>
                <ul>
                    <li>Plan ahead and start early. SQ3R takes time and is not a strategy that can be employed or used
                        effectively the night before a test.</li>
                    <li>Plan to read each chapter before it is discussed in class. Doing this will make the class
                        lecture a review. It is also likely to help you to understand the material that is presented in
                        class on a deeper level.</li>
                    <li>Identify information in the chapter that you don’t understand. Plan to talk with the professor
                        after class or during office hours if you still don’t understand the material after it is
                        covered in class.</li>
                </ul>
				<p>Write out your goals for using SQ3R over the next few weeks and stick to them.</p>
