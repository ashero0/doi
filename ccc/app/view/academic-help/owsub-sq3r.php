            <div class="card-body">
                <p>This step helps you gather the information that is necessary to focus on the chapter and formulate
                    questions for yourself as you read. It’s not necessary to have answers to your questions at this
                    step in the process – answers will come later.</p>
                <b>Read the title</b>
                <p>This helps your brain begin to focus on the topic of the chapter.</p>
                <b>Read the introduction and/or summary</b>
                <p>This orients you to how this chapter fits the author’s purposes. It also provides you with an
                    overview of the author’s statement of the most important points.</p>
                <b>Read each boldface heading and subheading</b>
                <p>This helps you to create a framework for the chapter in your mind before you begin reading. This
                    framework provides a structure for the thoughts and details to come.</p>
                <b>Review any graphics</b>
                <p>Charts, maps, diagrams, pictures, and other visual aids are there to make a point. Publishers will
                    not include these items in textbooks unless they significantly add to the content of the text.</p>
                <b>Review any reading aids in the chapter</b>
                <p>This includes italics, chapter objectives, definitions, and study questions at the end of the
                    chapter. These aids are there to help you sort, comprehend, and remember the material.</p>
