                    Read
                </button>
            </h5>
        </div>
        <div id="collapseThree" class="collapse" aria-labelledby="headingThree" data-parent="#accordion">
            <div class="card-body">
                <p>Reading each section fills in the information around the mental structures you have created by
                    surveying the chapter and developing questions about each heading.</p>

                <b>Read one section at a time</b>

                <p>As you read each section, look for the answers to your questions and write them down in your own
                    words on the right two-thirds of your piece of paper.</p>

                <b>Add more questions, if necessary</b>

                <p>A single question is probably adequate for a section that is only a few paragraphs long; however, for
                    longer sections, you may find that you need to add a question or two.</p>

                <b>Don’t get bogged down with the details</b>

                <p>Well-written textbooks often provide examples to further explain main ideas. As you read each
                    section, try to separate the details from the main ideas. Use the details to help you understand the
                    main ideas but don't expect yourself to memorize every detail provided in the chapter.</p>
            </div>
        </div>
