<h1>SQ3R Improving Reading Comprehension Workshop</h1>
<br>
<p>Textbooks are often read in the same way that other books are read for pleasure – beginning with the first page of
    the chapter and reading to the end of the chapter, without stopping. While this method is fine for novels, it is not
    likely to result in the level of understanding and retention needed for most textbooks and the classes they are
    assigned to. SQ3R provides a different method of reading textbooks that will most likely enhance understanding and
    retention of material. It's not a quicker way to read a chapter in a textbook, but it is likely to reduce the amount
    of time you will need to spend studying the material immediately prior to the test. This result comes from more time
    being spent actually understanding the chapter while it is initially read. Study time then becomes deepening your
    understanding of the material.</p>
<p>Reading textbooks and comprehending their information is difficult. If you want further information about reading
    comprehension, review other sections of Academic Support for Students. If you have been trying to improve your
    reading comprehension but the strategies you're trying just don't seem to be helping as much as you would like,
    please contact <a href="<?=BASE_URL?>about/contact-us" target="_blank">Cook Counseling Center</a>.</p>

<h4>SQ3R Process</h4>
<ol>
    <li>Survey</li>
    <li>Question</li>
    <li>Read</li>
    <li>Recite</li>
    <li>Review</li>
</ol>

