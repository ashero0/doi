        <div id="collapseFive" class="collapse" aria-labelledby="headingFive" data-parent="#accordion">
            <div class="card-body">
                <p>The review step helps to refine your mental organization of the material in the chapter and begin to
                    build memory – we learn through repetition. This step provides another opportunity for repetition of
                    the material and therefore will enhance your recall of the information.</p>
                <b>How do you review?</b>
                <p>Once you've finished reading the entire chapter, using the survey, question, read, and recite steps,
                    go back over all of your questions. Cover the answers to the questions that you’ve written down and
                    see if you can still recite them.</p>
                <b>What if some of the answers have been forgotten?</b>
                <p>Reread that section of the chapter to refresh your memory, recite the answer after you've
                    written it down, and then continue your review process.</p>
            </div>
