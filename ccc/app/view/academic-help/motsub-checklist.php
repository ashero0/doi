<h1>Motivation Checklist</h1>
<p>The following checklist may prove helpful in getting at the sources of poor motivation. If you want to improve your
    motivation, you may want to choose a self-directed improvement program or use the information as a focus for
    counseling.</p>

<b>1. Preferring something other than attending this university:</b>
<ul>
    <p>____Would prefer not to go to college.</p>

    <p>____Would rather attend another college.</p>

    <p>____Would prefer a different kind of training.</p>
</ul>
<b>2. College as means to an end other than learning:</b>
<ul>
    <p>____To avoid getting a job</p>

    <p>____To find a mate</p>

    <p>____To have a good time</p>

    <p>____To get away from home</p>

    <p>____To prove self-worth</p>
</ul>
<b>3. Distracting personal problems:</b>
<ul>
    <p>____Conflict with same sex</p>

    <p>____Conflict with opposite sex</p>

    <p>____Conflict with parents</p>

    <p>____Lack of confidence</p>

    <p>____Undefined resistance to college</p>

    <p>____Angry at the world</p>

    <p>____Overuse of drugs or alcohol</p>

    <p>____Fear of evaluation</p>

    <p>____Difficulty in making decisions</p>

    <p>____Lack of financial resources</p>

    <p>____Marriage problems</p>

    <p>____Phobias and other anxieties</p>

    <p>____Insecurity</p>

    <p>____Loneliness</p>
</ul>
<b>4. Lack of interest:</b>
<ul>
    <p>____Undefined vocational goals</p>

    <p>____Undefined educational goals</p>

    <p>____Course material is not what I think is important</p>

    <p>____Interest in school is not the "in" thing among my friends.</p>
</ul>
<b>5. Continuing self-defeating behavior patterns:</b>
<ul>
    <p>____Excessive dependence on parents or others</p>

    <p>____Fear as a motivator</p>

    <p>____Parents as motivators</p>

    <p>____Grades or academic achievement as motivator</p>

    <p>____High school habits</p>
</ul>
