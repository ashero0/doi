<h1>Strategies for Difficult Questions</h1>
<br>
<p>Most students experience difficult test questions during some point of their academic careers. There are several ways that you can break questions down and make them simpler and easier to answer with the knowledge you have available.</p>

<ul>
<li>Translate the questions into your own words</li>
<li>Read each option carefully</li>
<li>Use a strategy</li>
</ul>
<h4>Strategy Suggestions</h4>

<p>Attack unfamiliar words by sounding them out, by breaking them into familiar parts with meanings you know, and by looking at the surrounding words and sentences for clues to the meaning of the word.</p>

<p>Use your general knowledge. Ask yourself, "What do I already know about ____________"?</p>

<p>Anticipate the answer to the question you’ve just read and look for the one most like what you expect. Be flexible. You may not always find exactly the choice you are looking for. Then, you must choose the best of the options available.</p>

<p>Use logical reasoning. For each possible answer, ask yourself, "What would happen if… "?</p>

<b>Use the process of elimination for multiple-choice questions where more than one option is correct:</b>
<ul>
<li>Pick out the options you are sure are correct.</li>
<li>Pick out the options you are sure are incorrect.</li>
<li>Rule out all answers that contain wrong options.</li>
<li>Rule out all answers that omit options you are sure of.</li>
<li>Select the best remaining answer.</li>
</ul>
<p>Look for test-wiseness cues if the other strategies don't work. If you guess at an answer, check your guess against what you already know and against what your logical reasoning tells you.</p>

