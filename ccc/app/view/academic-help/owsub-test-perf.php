                <ol>
                    <li>Write everything the professor says</li>
                    <li>Write notes in an outline form</li>
                    <li>Write notes in a modified outline form</li>
                    <li>Copy everything appearing on slides</li>
                </ol>
                <p>One strategy for note taking that has proven to be very useful is the modified outline form, which
