                <b>Take practice tests</b>
                <p>Professors may have old tests that you can use to test yourself on current material. If they do, this
                    is an excellent way to prepare for your next test. If you complete the practice test a day or two in
                    advance of your next test, you can focus on the material you missed on the practice test during the
