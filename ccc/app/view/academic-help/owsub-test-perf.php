            <div class="card-body">
                <b>About a week before a test, develop a test preparation plan</b>
                <p>If you've practiced some or all of the techniques discussed previously, you are well on your way to
                    being prepared for any test. It is important to decide how many hours you need to allocate for
                    studying, and how to break those hours up, prior to your test.</p>
                <b>Review notes, problems, and textbooks</b>
                <p>Include time for reviewing your notes, homework problems, and textbook chapters, as well as other
                    materials the professor may have provided. Begin each study period by studying different material
                    than what you studied first during your previous study period. Also make sure you are not always
                    studying the same material in the middle or at the end of your study period. The "primacy effect"
                    suggests that we will best remember material studied first. The "recency effect" suggests that we
                    will best remember material studied last. Therefore, always studying the same material in the middle
                    of your study period suggests that you will be less likely to remember it.</p>
                <b>Concentrate on the main ideas using learning objectives, study questions, and other chapter study
                    aids to help</b>
                <p>If test questions will be coming from the textbook as well as class lectures, use the study aides in
                    the textbook chapters to guide you. These aids were created to highlight the ideas the author
                    perceived to be the main points in the chapter. In many cases, your professor will probably focus on
                    these ideas as well.</p>
