                <b>Connect class work to long-range goals*</b>
                <p>Some students love to learn just for the sake of learning, but many more need to believe that what
                    they
                    are doing in class has a purpose in the future. If you are one of those students in the latter
                    group,
                    try to apply what you learn in class to practical situations or your daily life.</p>
                <b>Become active and involved in learning*</b>
                <p>It is important to make learning an active process as opposed to a passive one. Making a learning
                    situation an active one requires that you actually process what you are hearing or reading. Put it
                    into
                    your own words to make the material more meaningful to you.</p>
                <b>Identify preferred learning style*</b>
                <p>Different people have different learning styles. Rita Dunn's classification of learning styles
                    includes:
                    visual, auditory, and kinesthetic/tactile. David Kolb and Bernice McCarthy identify learning styles
                    as
                    imaginative, analytic, common sense, and dynamic. It is important to try to identify your preferred
                    learning style and use that as much as possible in your study process. This will increase your
                    retention
                    of the material.</p>
                <b>Practice patience with the learning process and your progress*</b>
                <p>Learning takes time. Yes, there are students that seem to have a photographic memory and never need
                    to
                    study, but that is not true for most people. Students must learn how long it takes to learn
                    material,
                    while allowing adequate time for this learning to take place, to be a successful student.</p>
                <b>Use multiple methods for learning*</b>
                <p>We learn through repetition; however, that doesn't mean that we only need to study in one way.
                    Students
                    learn best by using a variety of strategies, including reviewing notes, making flashcards,
                    completing
                    homework problems, outlining chapters in the textbook, or studying in groups. The most effective
                    methods
                    used will vary depending on the class. </p>
                <b>Think about what is learned from the “big picture” perspective*</b>
                <p>It is important to learn not only the facts, but to understand how these facts fit into the bigger
                    picture. How do the facts you are learning apply to the chapter you are reading and the subject you
                    are
                    studying?</p>
                <b>* Phrase quoted from: Walther, D. R. (1994). Toolkit for College Success. Belmont, CA: Wadsworth.</b>
