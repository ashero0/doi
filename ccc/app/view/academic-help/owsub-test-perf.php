                <b>Use chapter headings, other chapter aids, your textbook, and class notes to develop your own test</b>
                <p>If your professor doesn't have old tests available, consider making your own test by using provided
                    and created aids. This will take time, but the may lead to performing better on your test.</p>
                <b>Create test-like conditions</b>
                <p>A critical part of self testing is to take the test under test-like conditions. If you will not be
                    able to use notes or your textbook during the test, don't use them when you take the practice test.
                    Also, try to do the practice test within the time limit you will have on the real test. In this way,
                    you get an idea whether or not you know the material well enough, or whether you need to spend more
                    time studying the material so that you can recall it more quickly.</p>
