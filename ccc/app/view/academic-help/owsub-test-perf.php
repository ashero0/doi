<h1>Improving Test Performance Workshop</h1>
<br>
<p>Test performance not only reflects how much you studied in the hours and days leading up to the test, but also how
    well you have been studying throughout the semester. This page offers a variety of study strategies that you can use
    from the first day of class to improve your test performance over the entire semester. You may find, as you read
    through this page, that you are already routinely practicing some of these skills, while there may be others that
    you're not practicing as regularly. It is encouraged that you identify one or two skills you would like to try to
    routinely incorporate into your study schedule.</p>

