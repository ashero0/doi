                <b>What study strategies have you tried for improving your grades?</b>
                <p>There are numerous study strategies and no single strategy is likely to work best for all of your
                    classes. Strategies might include taking notes on chapters in the textbook as you read them,
                    reviewing your notes each day after class, reworking homework problems under test-like conditions,
                    or testing yourself on the notes you have taken in class. Experimenting with various strategies that
                    seem appropriate for different classes will help you decide which strategies work best for those
                    classes. Your learning style will also have an impact on the study methods that help you learn most
                    effectively. Some people prefer to study alone while others learn better in groups. Some people
                    learn through auditory means and benefit from talking aloud as they study. Some people are visual
                    learners and need to see what they are learning. Think about how you learn best and try to include
                    those learning methods in your study process as much as possible to enhance you learning.</p>
                <b>What strategy or combination of strategies have you used most effectively to prepare for tests?</b>
                <p>Just as there are a variety of study strategies, there are also a variety of strategies that are
                    useful for preparing for tests. It is important to know if you will be taking a multiple choice,
                    essay, or problem solving test because this will have an impact on the way you study. Study
                    strategies for multiple-choice tests may include reviewing notes after class each day, carefully
                    reading the textbook, taking notes as you read, and making flashcards with specific information you
                    are expected to learn. Studying for an essay exam might include reviewing notes daily, reading and
                    reviewing the textbook, trying to understand the "big picture," and making sure you can support
                    general ideas with specific points. Preparing for a problem solving test may involve reworking
                    homework problems, completing extra problems, meeting with a friend to talk through when to apply
                    different problem solving methods, creating your own test, or taking an old test from the professor
                    under test-like conditions to assess whether you know the material well enough to complete your
                    upcoming test within the time that will be allowed. There are many other strategies out there.
                    Finding strategies that work for you and allowing adequate study time to implement the strategies
                    for each class is key.</b>
