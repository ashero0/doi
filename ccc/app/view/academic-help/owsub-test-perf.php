            <div class="card-body">
                <b>Survey</b>
                <p>Read the chapter title, introduction, learning objectives, summary, and end of chapter questions.
                    Also review bold face headings and subheadings, graphics, and italics throughout the chapter. This
                    will provide an overview of the chapter and create a mental framework for understanding the chapter
                    more thoroughly as you read.</p>
                <b>Question</b>
                <p>Prior to reading each section, turn each heading into one or more questions. It will be helpful if
                    you write your questions down so you can refer to them as you move through this process. This step
                    helps one's mind engage and concentrate and creates an active learning environment.</p>
                <b>Read</b>
                <p>As you read each section, look for answers to your questions. This fills in the information around
                    the mental framework you have created in the previous steps.</p>
                <b>Recite</b>
                <p>After you have finished reading a section and before moving on to the next section of the chapter, go
                    back to your questions to see if you can answer them. If you can't, you may have missed the main
                    ideas in that section and may want to read it again. If you can answer them, you're ready to
                    complete this process for the next section.</p>
                <b>Review</b>
                <p>Review all of your questions to see if you can answer them. This step helps to refine your mental
                    organization and begin to build your memory and understanding of the material. Following this model
                    as you read chapters is likely to reduce the amount of time you will need to spend studying the
                    chapters the week before the test because you have already begun to learn and memorize the material.
                </p>
