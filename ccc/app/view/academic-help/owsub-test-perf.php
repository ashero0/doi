        <div id="collapseTwelve" class="collapse" aria-labelledby="headingTwelve" data-parent="#accordion">
            <div class="card-body">
                <b>Arrive on time</b>
                <p>Arriving on time will help to avoid the "brain pickers," other students who ask you questions right
                    before the test to that you may or may not know the answer to. This can create needless anxiety.</p>
                <b>Do a memory dump</b>
                <p>If you believe that there will be a discrete amount of information that you will most likely need to
                    know on the test and you fear you may forget it, write it down on the test as soon as the test is
                    distributed. Then if you need this information, you have it available and do not need to rely on
                    your memory.</p>
                <b>Read directions carefully</b>
                <p>The directions for a test may seem obvious to some, but to others, there may be valuable information
                    hidden in them. Read the directions carefully to ensure that you understand what you are being asked
                    to do as you respond to the questions.</p>
                <b>Budget time wisely</b>
                <p>Quickly review the test before you begin and decide how much time you will spend on each part of the
                    test. Pay attention to the weight of each section on your grade and make sure you allow enough time
                    for sections that may take more time or are weighted more heavily in the final test grade. It's
                    never fun to arrive at the last page of the test only to find an essay question worth 25 percent of
                    your grade with only a few minutes remaining to complete the test.</p>
                <b>Feeling anxious? Practice relaxation</b>
                <p>A number of autogenic relaxation techniques can be useful during a test if you find that anxiety is
                    interfering with your performance. Some anxiety is good because it helps us perform better, but at
                    some point, the anxiety can reduce performance. If you are unfamiliar with autogenic relaxation
                    techniques, you may wish to talk with a counselor in your counseling center to find out more about
                    these techniques.</p>
                <b>Look for cues</b>
                <p>You can look for two answers that are similar, cues from other questions, grammatical matching
                    between the question and answers, as well as other strategies to give you a hint at correct answers.
                </p>
                <b>Answer all of the questions</b>
                <p>Some professors give partial credit, so it is important to answer all of the questions even if you
                    are running out of time and can’t answer them fully.</p>
                <b>Do not change answers</b>
                <p>Research has shown that it is best to go with our first instinct when choosing an answer to a
                    question, unless you are very sure the answer you have chosen is wrong.</p>
                <b>Use all of the time available</b>
                <p>If you finish early, go back and review your answers to make sure you haven't skipped a question or
                    made careless mistakes as you responded to the questions.</p>
