                    <li>A week before a test</li>
                    <li>Two days before a test</li>
                    <li>The night before a test</li>
                </ol>
                <p>While you may not actually label what you are doing as “studying for a test” until a few days or a
                    week before the test, everything you do from the first day of class or the first day after a test is
                    helping you prepare for the next test. Class attendance, note taking, reviewing notes, completing
                    homework assignments, reading the textbook, and reviewing notes and assignments prior to a test all
                    contribute to how you perform on the test.</p>
