<h1>Scheduling Strategies</h1>
<br>
<h3>Long Term Schedule</h3>

<p>Construct a schedule of your fixed commitments only, only those obligations that you are required to meet every week
    (job hours, classes, church, club meetings, etc.).</p>

<h3>Intermediate Schedule - One per week</h3>

<p>Now make a short list of major events and the amount of work to be accomplished in each class this week. This may
    include non-study activities.</p>
For example:
<ul>
    <li>Quiz Wednesday</li>
    <li>Paper Tuesday</li>
    <li>Ball game Tuesday night</li>
    <li>Finish 40 pages in English by Friday</li>
    <li>Finish 150 pages in history by Friday</li>
</ul>
<p>These events will change from week to week and it is important to make a new list for reach week. Sunday night may be
    the most convenient time to do this.</p>

<h3>Short Term Schedule - One per day</h3>
<p>On a small note card each evening before retiring, or early in the morning, write out a specific, daily schedule.
    Write down specifically what is to be accomplished. Such a schedule might include:</p>
Wednesday
<ul>
    <li>8:00 – 8:30 a.m.: Review history</li>
    <li>9:30 – 10:30 a.m.: Preview math and prepare for quiz</li>
    <li>4:45 p.m.: Pick up cleaning on way home</li>
    <li>7:00 – 10:15 p.m.: Chapters five and six (history)</li>
    <li>10:30 p.m.: Phone calls</li>
</ul>
<p>Carry this card with you and cross out each item as you accomplish it. Writing down things in this manner not only forces you to plan your time, but also causes you to make a promise to yourself to do what you have written down.</p>
