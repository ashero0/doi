<h1>Constructive Suggestions for Motivation</h1>
<p>It is your responsibility to make college a growing experience for yourself. The following suggestions and resources
    may be helpful to developing a consistent system for motivating yourself through your university career. When you
    come across issues that you find you might be slow to address or overwhelmed with, it is necessary to have a
    strategy for motivating yourself to face them and move forward in a positive way.</p>
<h4>Attack the problem of goal setting directly</h4>

<b>Gather information about your interests, abilities, values, and needs. The following may be useful in this
    process:</b>
<ul>
    <li>Tests</li>
    <li>Counseling</li>
    <li>Self-evaluation</li>
</ul>

<b>Gather information about occupations:</b>
<ul>
    <li><a href="https://career.vt.edu/" target="_blank">Career and Professional Development</a> at Virginia Tech has
        files of this type of information.</li>
    <li>Use the <a href="https://www.bls.gov/ooh/" target="_blank">Occupational Outlook Handbook.</a></li>
    <li>Talk to personnel in Career Services.</li>
    <li>Get first-hand information from people working in this field.</li>
</ul>

<b>Gather information about training requirements and training programs:</b>
<ul>
    <li>Using Career and Professional Development occupational files</li>
    <li>Using college and other catalogs</li>
    <li>Get first-hand information from professors or those in the field</li>
</ul>

<p>On the basis of the information you find, start making decisions that lead you in directions you want to go.</p>
<h4 style="padding-top: 20px; padding-bottom: 10px;">Attack personal problems directly</h4>

<b>Using the services of Cook Counseling Center:</b>
<ul>
    <li>Individual counseling or therapy</li>
    <li>Self-help materials</li>
    <li>Group counseling or therapy</li>
</ul>
<b>Attack the problem yourself:</b>
<ul>
    <li>Self-evaluation</li>
    <li>Direct confrontation of others involved in the problem</li>
    <li>Accept responsibilities for initiating changes you want to make</li>
</ul>

