<h1>Study Skills Checklist</h1>
<br>
<p>Make a print out of this document. Read each statement and consider how it applies to you. If it does apply to you,
    check Y. If it does not apply to you, check N. The purpose of this inventory is to find out about your own study
    habits and attitudes.</p>
<h4>Time Scheduling</h4>
<ol>
    <p>1. Y__ N__ I spend too much time studying for what I am learning.</p>
    <p>2. Y__ N__ I usually spend hours cramming the night before an exam.</p>
    <p>3. Y__ N__ If I spend as much time on my social activities as I want to, I don't have enough time left to
        study, or
        when I study enough, I don't have time for a social life.</p>
</ol>

<h4>Concentration</h4>
<ol>
    <p>Y__ N__ I usually try to study with the radio and TV turned on.</p>
    <p>Y__ N__ I can't sit and study for long periods of time without becoming tired or distracted.</p>
    <p>Y__ N__ I go to class, but I usually doodle, daydream, or fall asleep.</p>
</ol>

<h4>Listening and Note Taking</h4>
<ol>
    <p>Y__ N__ My class notes are sometimes difficult to understand later.</p>
    <p>Y__ N__ I usually seem to get the wrong material into my class notes.</p>
    <p>Y__ N__ I don't review my class notes periodically throughout the semester in preparation for tests.</p>
</ol>

<h4>Reading</h4>
<ol>
    <p>Y__ N__ When I get to the end of a chapter, I can't remember what I've just read.</p>
    <p>Y__ N__ I don't know how to pick out what is important in the text.</p>
    <p>Y__ N__ I can't keep up with my reading assignments, and then I have to cram the night before a test.</p>
</ol>

<h4>Testing and Exams</h4>
<ol>
    <p>Y__ N__ I lose a lot of points on essay tests even when I know the material well.</p>
    <p>Y__ N__ I study enough for my test, but when I get there my mind goes blank.</p>
    <p>Y__ N__ I often study in a haphazard, disorganized way under the threat of the next test.</p>
</ol>

<h4>Reading</h4>
<ol>
    <p>Y__ N__ I often find myself getting lost in the details of reading and have trouble identifying the main ideas.
    </p>
    <p>Y__ N__ I rarely change my reading speed in response to the difficulty level of the selection, or my familiarity
        with the content.</p>
    <p>Y__ N__ I often wish that I could read faster.</p>
</ol>

<h4>Writing Skills</h4>
<ol>
    <p>Y__ N__ When my teachers assign papers I feel so overwhelmed that I can't get started.</p>
    <p>Y__ N__ I usually write my papers the night before they are due.</p>
    <p>Y__ N__ I can't seem to organize my thoughts into a paper that makes sense.</p>
</ol>

<p>If you have marked Y on two or more questions in any category, look at our Study Skills Information for
    those categories. If you have one "yes" or less in a category, you are probably proficient enough in these areas
    that you don't need Self-help Information. Feel free, however, to get information in areas that you may have special
    interests, even if you scored well.</p>