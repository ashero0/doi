<h1>List of Group Counseling Programs</h1>

<br>
<h3>Understanding Self and Others</h3>
<p>This group is for students who wish to increase self-understanding, self-esteem, and intimacy in
    relationships, as well as addressing their interpersonal style and relationship-building skills. This is
    an opportunity to receive support and feedback from other students in a safe environment while
    developing your ability to understand and express yourself. Both undergraduate and graduate groups are
    available</p>

<h3>Substance Use Group</h3>
<p>This group is for students who have concerns about their substance use behaviors. Students are
    encouraged to set personal goals for their use based on the stages of change model.</p>

<h3>DBT Skills Group</h3>
<p>This group is a psychoeducation group based upon Dialectical Behavioral Therapy. It teaches skills in
    Mindfulness, Distress Tolerance, Emotion Regulation, and Interpersonal Effectiveness.</p>

<h3>DBT Skills Group for Eating Disorders</h3>
<p>This group is structured to help those who struggle with eating issues learn effective coping skills in
    order to reduce suffering and improve self-acceptance and general life satisfaction.</p>

<h3>Working Through Trauma</h3>
<p>This support group is being offered by the Cook Counseling Center and the Women's Center. This group is
    for female-identified survivors of sexual trauma or violence who may benefit from peer support and
    education.</p>

<h3>The Moose Group</h3>
<p>This group is for students who have experienced severe mental illness and/or hospitalization. It is open
    to those who are interested in receiving support, feedback, and increasing their understanding of how
    their mental illness affects their lives and their relationships.</p>

<h3>Mindfulness for Anxiety</h3>
<p>This group teaches mindfulness practices that help manage anxiety. Each week, members practice
    mindfulness exercises and discuss how they are moving through their anxiety to pursue the things that
    matter to them.</p>

<h3>Grief Group</h3>
<p>This group is for individuals who have experienced the death / imminent death of a loved one. It
    provides a combination of psychoeducation and discussion in a safe, nonjudgmental space with others who
    are there to listen.</p>

<h3>Body Movement Group</h3>
<p>This is a group guided physical yoga and meditation practive to use as tools for anxiety management. It
    incorporates supportive conversation to encourage self-reflection and connection. Individuals are
    required to participate in concurrent individual therapy.</p>

<h3>Social Skills Gaming Group</h3>
<p>This group uses a fantasy role-playing game to help individuals with Autism Spectrum Disorder develop
    social skills. Including perspective-taking, tolerating frustration, problem-solving, communication
    and collaboration.</p>

<h3>Expressive Arts with Wagner</h3>
<p>This class engages members in different art modalities to promote stress management and personal growth.
    This group facilitates creativity, self-reflection and connection. No previous artistic experience
    is necessary.</p>

<h3>Our Voice</h3>
<p>A support group, held outside of Cook Counseling, for any student of a marginalized racial or ethnic
    background to glean space to explore shared difficulties pertaining to their identities.</p>

<h3>Facing Our Fears</h3>
<p>This focuses on systematically confronting worrying thoughts in order to decrease obsessive thought
    or compulsive thoughts / behaviors. This group is for anyone who has repetitive worries or behaviors
    that get in the way of their daily functioning.</p>