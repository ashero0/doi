        professionals, medical providers, or legal advisers. MHAV’s Warm Line can provide support for those who need
        someone to talk to, however crisis situations should be directed to another of the listed resources.</li>
    <li><a href="https://suicidepreventionlifeline.org/" target="_blank">National Suicide Prevention Lifeline</a> -
    <b>1-800-273-8255 (1-800-TALK)</b>. Available 24 hours a day</li>
