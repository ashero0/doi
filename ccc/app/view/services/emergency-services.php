<h3><i>Worried about Self-Harm or Suicidality?</i></h3>
<p>If you are concerned about yourself or any Virginia Tech student being (or becoming) suicidal,
    please call the Cook Counseling Center at <b>540-231-6557</b> or one of the other resources listed below. The Cook
    Counseling Center number can be used during regular office hours or after-hours to speak with a counselor.
</p>
<h3><i>Local Crisis and Support Resources</i></h3>
<ul>
    <li><a href="http://www.nrvcs.org/access-services/" target="_blank">ACCESS</a> - NRVCS (New River Valley
        Community Services) Emergency Services provides emergency evaluation and crisis intervention to the New
        River Valley 24 hours per day, 7 days per week. A friend or family member can call for you if you feel that
        you are unable to do so yourself. Call <b>540-961-8400</b>.</li>
    <li><a href="http://www.nrvcs.org/raft/" target="_blank">RAFT</a> - The Raft Crisis Hotline is a program of
        NRVCS that offers free paraprofessional phone counseling services to the community.Volunteers provide
        suicide and crisis intervention, empathy and support, mental health and substance abuse information and
        referrals to the local communities including Virginia Tech and Radford University. Raft operates 4 p.m. – 8
        a.m. Monday through Friday and 24 hours on the weekends. NRVCS Case Managers answer the phones during
        business hours and New Horizons, a residential crisis stabilization program, provides callers with services
        when volunteers are unavailable. Call <b>540-961-8400</b>.</li>
    <li><a href="https://www.carilionclinic.org/connect" target="_blank">CONNECT</a> - A 24-hour referral and
        emergency evaluation service of Carillion Health Care. Call <b>1-800-284-8898</b> or <b>540-981-8181</b>.</li>
    <li><a href="https://lewisgale.com/specialties/inpatient-mental-health-care/" target="_blank">ARC</a> - A mental
        health admittance and referral service of Montgomery Regional Hospital and Lewis Gale Hospital. Call
        <b>804-327-5191</b> or <b>877-886-7026.</b></li>
    <li><a href="https://police.vt.edu/index.html" target="_blank">Virginia Tech Police</a> – <b>911</b> if on campus or
    <b>540-231-6411</b></li>
</ul>
<h3><i>State and National Crisis and Support Resources</i></h3>
<ul>
    <li><a href="https://mhav.org/support/warm-line/" target="_blank">Mental Health America of Virginia Warm
