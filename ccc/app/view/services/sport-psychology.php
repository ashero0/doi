<h1>Sport Psychology</h1>
<br>
<p>The Virginia Tech Athletics Department contracts with the Thomas E. Cook Counseling Center to provide counseling
    services for student-athletes through the Counseling & Athletic Mental Performance (CAMP) office. Located in the
    Merryman Athletic Center, the Sport Psychology Program provides personal counseling, performance enhancement and
    psychological skills training, psychological rehabilitation from injury, assessment, and consultation with coaching,
    medical, training, and administrative staff. Mental skills training and team building activities are also available
    for
    teams. We also work with the Leadership Institute within the Department of Athletics.</p>

