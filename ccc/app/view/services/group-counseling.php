<h1>Group Counseling</h1>

<p>Group counseling is one of our primary clinical services. For many students, group therapy is the most
    effective and appropriate treatment option.

    Group counseling is a unique opportunity available to students in need of a supportive environment. It
    allows students to hear multiple perspectives and receive encouragement and feedback from peers in a safe
    and confidential environment. These interpersonal interactions deepen self-awareness and help members learn
    new ways of relating to others. While some of our groups focus primarily on deepening this understanding of
    self and others, we also have groups that focus on specific concerns and skill development.

    There is no limit to the number of group therapy sessions available to students. Getting started with group
    counseling begins with an initial evaluation, or intake appointment. You and your counselor will discuss if
    group counseling is the appropriate form of treatment. Your counselor will also provide information about
    our groups so you can choose the one that best suits your needs. Groups meet weekly for 60-90 minutes. There
    are typically 5-12 students in each group and 1-2 group leaders.

    Check out our videos and the links below for more information about our group program. </p>

