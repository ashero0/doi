<h1>Individual Counseling</h1>

After the initial assessment appointment, you and your counselor may agree that a course of individual therapy is the
most appropriate treatment for you. You and the therapist will identify an area of concern for your work and establish
goals for therapy. On average, students are seen for 4-5 sessions, meeting every 2-3 weeks. In the majority of cases,
this will meet your needs. If you would like to do additional work, you may be referred to the group therapy program.
For problems requiring more extensive work, a referral to a community provider is recommended.

<br>
<br>
<h3>Student Difficulties Might Include</h3>
<ul>
    <li>Anxiety</li>
    <li>Depression</li>
    <li>Eating issues</li>
    <li>Loneliness</li>
    <li>Feelings of inadequacy</li>
    <li>Problems studying</li>
    <li>Problems relating to others</li>
    <li>Sexual concerns</li>
    <li>Excessive use of alcohol and/or drugs</li>
    <li>Couples issues</li>
</ul>

