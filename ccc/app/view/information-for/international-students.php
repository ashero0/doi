<h3>Counseling center staff defined:</h3>

<p>A professional counselor</p>
<ul>
    <li>has a Ph.D.or M.Ed in counseling or social work</li>
    <li>has been licensed by the state of Virginia,</li>
    <li>provides individual and group counseling, and</li>
    <li>supervises graduate student trainees.</li>
</ul>

<p>A psychologist</p>
<ul>
    <li>has a Ph.D.or Psy.D. in counseling or clinical psychology,</li>
    <li>has been licensed by the state of Virginia,</li>
    <li>provides individual and group counseling, and</li>
    <li>supervises graduate student trainees.</li>
</ul>

<p>A psychiatrist</p>
<ul>
    <li>has a medical degree and has training in prescribing medicine for problems such as depression and anxiety,</li>
    <li>has been licensed by the state of Virginia, and</li>
    <li>meets briefly with students who want medication to help with counseling.</li>
</ul>

<p>A nurse practitioner</p>
<ul>
    <li>has a master’s degree in nursing and has training in prescribing medicine for problems such as depression and
        anxiety,</li>
    <li>has been licensed by the state of Virginia, and</li>
    <li>meets briefly with students who want medication to help with counseling.</li>
</ul>

<p>A graduate student trainee</p>
<ul>
    <li>is working toward a master’s or doctoral degree in counseling or psychology,</li>
    <li>is supervised by licensed counseling center staff, and</li>
    <li>provides individual and group counseling.</li>
</ul>

<h3>Services are offered by Cook Counseling Center</h3>

<p>We provide individual and group counseling as well as psychiatric, consultation, and outreach services to the
    Virginia Tech community. Services are available to all currently enrolled Virginia Tech students who pay the student
    health fee. The counseling center’s services are protected by law and information about your treatment can only be
    released to others with your written permission; however, confidentiality can be broken for serious concerns for
    safety to self or others, or if the records are court ordered. Below you can find descriptions and reasons for why
    you might want to use Cook Counseling Center’s services.</p>

<p>Personal counseling:</p>

<ul>
    <li>To talk about your stress or difficulties</li>
    <li>To gain other perspectives</li>
    <li>To help with adjustment to U.S. culture and the educational system</li>
    <li>To overcome loneliness</li>
    <li>To communicate better</li>
</ul>

<p>Group counseling:</p>

<ul>
    <li>To share your experience</li>
    <li>To gain support</li>
    <li>To improve relationship skills</li>
    <li>To gain other perspective on your stress</li>
</ul>

<p>Consultation:</p>

<ul>
    <li>To better understand the counseling center's services</li>
    <li>To learn about other campus resources that can help with a problem</li>
</ul>

