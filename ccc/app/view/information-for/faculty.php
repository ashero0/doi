<h1>Information for Faculty + Staff</h1>

<p>The college years can be very stressful for many students. In the contemporary climate of competition and pressure,
    some students adequately cope with these stresses while others find that stress becomes unmanageable and interferes
    with learning. In some cases, these students may eventually disrupt the learning of others.</p>

<p>As a member of Virginia Tech’s faculty, you have a unique view into the lives of your students. Many students
    initially seek assistance from faculty or staff members when dealing with an unmanageable stressor. It is important
    that you welcome students to speak to you about what may be bothering them and, if necessary, direct them toward the
    Cook Counseling Center.</p>

<p>If a student exhibits signs of a life-threatening situation, whether to themselves or to others, direct this
    information to the Virginia Tech Police Department at 540-231-6411.</p>

<h3>Identifying and Referring a Distressed Student</h3>

<p><u>Guidelines for Identifying Distressed Students</u></p>
<ul>
    <li>Excessive procrastination and very poorly prepared work, especially if inconsistent with previous work</li>
    <li>Infrequent class attendance with little or no work completed</li>
    <li>Dependency (e.g., the student who hangs around or makes excessive appointments during office hours)</li>
    <li>Listlessness, lack of energy, or frequently falling asleep in class.</li>
    <li>Marked changes in personal hygiene</li>
    <li>Impaired speech and disjointed thoughts</li>
    <li>Repeated requests for special consideration (e.g., deadline extensions)</li>
    <li>Threats to others.</li>
    <li>Expressed suicidal thoughts (e.g., referring to suicide as a current option)</li>
    <li>Excessive weight gain or loss</li>
    <li>Behavior that regularly interferes with effective class management</li>
    <li>Frequent or high levels of irritable, unruly, abrasive, or aggressive behavior</li>
    <li>Unable to make decisions despite your repeated efforts to clarify or encourage</li>
    <li>Bizarre behavior that is obviously inappropriate for the situation (e.g., talking to something/someone that is
        not present)</li>
    <li>Students who appear overly nervous, tense or tearful</li>
</ul>

<p><u>Guidelines for Interaction</u></p>
<ul>
    <li>Talk to the student in private</li>
    <li>Express concern – Be as specific as possible in stating your observations and reasons for concern</li>
    <li>Listen carefully to everything the student says</li>
    <li>Repeat the essence of what the student has told you so your attempts to understand are communicated</li>
    <li>Avoid criticizing or sounding judgmental</li>
    <li>Consider Cook Counseling Center as a resource and discuss referral with the student</li>
    <li>If the student resists referral and you remain uncomfortable with the situation, contact Cook Counseling Center
        to discuss your concern</li>
</ul>

