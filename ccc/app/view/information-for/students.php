<h3>Worried about Self-Harm or Suicidality?</h3>

<p>If you are concerned about yourself or any Virginia Tech student being (or becoming) suicidal, please call the Cook Counseling Center at 540-231-6557 or one of the other resources listed below. The Cook Counseling Center number can be used during regular office hours or after-hours to speak with a counselor.</p>

<h3>Local Crisis / Support Resources:</h3>

<p><a href="http://www.nrvcs.org/access-services/">ACCESS</a> – NRVCS (New River Valley Community Services) Emergency Services provides <b>emergency evaluation and crisis intervention</b> to the New River Valley 24 hours per day, 7 days per week. A friend or family member can call for you if you feel that you are unable to do so yourself. Call 540-961-8400.</p>

<p><a href="http://www.nrvcs.org/raft/">RAFT</a> - The Raft Crisis Hotline is a program of NRVCS that offers <b>free paraprofessional phone counseling services</b> to the community.Volunteers provide suicide and crisis intervention, empathy and support, mental health and substance abuse information and referrals to the local communities including Virginia Tech and Radford University. Raft operates 4 p.m. – 8 a.m. Monday through Friday and 24 hours on the weekends. NRVCS Case Managers answer the phones during business hours and New Horizons, a residential crisis stabilization program, provides callers with services when volunteers are unavailable. Call 540-961-8400</p>

<p><a href="https://www.carilionclinic.org/connect">CONNECT</a> – A 24-hour <b>referral and emergency evaluation service</b> of Carillion Health Care. Call 1-800-284-8898 or 540-981-8181.</p>

<p><a href="https://lewisgale.com/service/mental-health-programs-and-services">ARC</a> – A <b>mental health admittance and referral service</b> of Montgomery Regional Hospital and Lewis Gale Hospital. Call 804-327-5191 or 877-886-7026.</p>

<p><a href="https://www.police.vt.edu/index.html">Police</a> – 911</p>

<h3>State / National Crisis & Support Resources:</h3>

